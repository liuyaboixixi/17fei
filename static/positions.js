$(function () {
    let vip = localStorage.getItem("vip") || true; // 默认解锁VIP
    let length = 10;
    if (vip) {
        length = 100;
        $("#position-count").text(100);
        $("#goabout").hide();
    }
    $("#card").on("change", () => {
        $("#position").attr("src", "");
        if (!$("#card")[0].checked) {
            new Howl({
                src: "/Gold2.wav",
                format: ["mp3"],
                autoplay: true,
                onload: function () {
                    window.voice = null;
                },
            });
            let src = atob(positions[Math.floor(length * Math.random())]);
            setTimeout(function () {
                $("#position").attr("src", src);
                
                // 显示中文名称 - 直接根据文件名映射
                const filename = src.split('/').pop();
                const chineseName = getChineseNameByFilename(filename);
                
                // 创建或更新中文名称显示
                let nameDisplay = $("#position-name");
                if (nameDisplay.length === 0) {
                    nameDisplay = $('<div id="position-name" style="position: absolute; top: -30px; left: 50%; transform: translateX(-50%); background: rgba(0,0,0,0.3); color: white; padding: 4px 8px; border-radius: 10px; font-size: 14px; font-weight: bold; z-index: 10; white-space: nowrap;"></div>');
                    $("#position").parent().css("position", "relative").append(nameDisplay);
                }
                nameDisplay.text(chineseName);
            }, 200);
        }
    });
});
