// 获取指定等级的任务
export function getTasksByLevel(levelId: string): string[] {
  const level = taskLevels.find(l => l.id === levelId);
  return level ? level.tasks : [];
}

// 获取随机任务
export function getRandomTask(levelId: string): string {
  const tasks = getTasksByLevel(levelId);
  if (tasks.length === 0) return "暂无任务";
  return tasks[Math.floor(Math.random() * tasks.length)];
}

// 获取多个随机任务（用于任务组装）
export function getRandomTasks(levelId: string, count: number = 3): string[] {
  const tasks = getTasksByLevel(levelId);
  if (tasks.length === 0) return ["暂无任务"];
  
  const shuffled = [...tasks].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, Math.min(count, tasks.length));
}

// 获取所有等级信息
export function getAllLevels(): TaskLevel[] {
  return taskLevels;
}

// 根据解锁等级获取可用等级
export function getAvailableLevels(unlockLevel: number): TaskLevel[] {
  return taskLevels.filter(level => level.unlockLevel <= unlockLevel);
}

