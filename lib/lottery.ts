// 根据幸运数字获取任务
export function getTaskByLuckyNumber(luckyNumber: string): LotteryTask | null {
  // 简单的哈希算法，将幸运数字映射到任务
  const hash = luckyNumber.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
  const taskIndex = hash % lotteryData.tasks.length;
  return lotteryData.tasks[taskIndex];
}

// 验证幸运数字格式
export function validateLuckyNumber(number: string): boolean {
  return /^\d{4}$/.test(number);
}

// 生成随机幸运数字
export function generateRandomLuckyNumber(): string {
  return Math.floor(1000 + Math.random() * 9000).toString();
}


